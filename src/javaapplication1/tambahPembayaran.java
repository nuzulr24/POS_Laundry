    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        // TODO add your handling code here:
        String metodes = metode.getText();
        String stats;
        if(status.getSelectedItem() == "Aktif") {
            stats = "Aktif";
        } else {
            stats = "Tidak Aktif";
        }
        
        try {
            if(metodes == null) {
               JOptionPane.showMessageDialog(this,"Tidak ada Inputan","Gagal",JOptionPane.ERROR_MESSAGE);
            } else {
            sql = "insert into tbl_pembayaran (metode,status) values('"+metodes+"','"+stats+"')";
            st.executeUpdate(sql);
            JOptionPane.showMessageDialog(this,"Data berhasil disimpan","Success",JOptionPane.INFORMATION_MESSAGE);
            this.setVisible(false);
            new ManajemenPembayaran().setVisible(true);
            new ManajemenPembayaran().setTitle("Manajemen Pembayaran");
            }
        } catch (Exception e) {
            System.out.println("Error"+stats+"metods"+metodes+e.getMessage());
        }
    }//GEN-LAST:event_tambahActionPerformed

